'''Returning the sum, subtraction, multiplication, division'''

def add(c,d):
    '''Returns the sum'''
    return c + d

def subtract(c,d):
    '''Returns the subtraction'''
    return c - d

def multiply(c,d):
    '''Returns the multiplication'''
    return c * d

def divide(c,d):
    '''Returns the division'''
    return c / d
